// Build-time data generation for the Rust online server (npm run gen-server-data).
// Emits the server's copy of the country table, map-eligible ids and flag-twin
// groups so the dedicated server stays byte-for-byte in sync with the client:
//   - server/src/game/countries.rs   COUNTRIES + MAP_IDS
//   - server/src/game/flag_twins.rs  TWIN_GROUPS
//
// The server must pick the flag sequence and score answers identically to the
// client, so all three data sets are derived from the SAME sources the client
// uses (src/data/countries.json, the bundled TopoJSON, and the flag-twin files).
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const readJson = (rel) => JSON.parse(readFileSync(resolve(root, rel), 'utf8'));

// --- sources ---------------------------------------------------------------
const countries = readJson('src/data/countries.json');
const topo = readJson('src/assets/countries-110m.json');

// Pull the array literals out of the TS twin files. They're JSON-shaped once the
// single quotes become double quotes, so we can parse them without importing TS.
function extractArrayLiteral(text, name) {
  const m = text.match(new RegExp(`${name}[^=]*=\\s*(\\[[\\s\\S]*?\\]);`));
  if (!m) throw new Error(`could not find ${name}`);
  const json = m[1]
    .replace(/'/g, '"')
    .replace(/,(\s*[\]}])/g, '$1'); // drop trailing commas
  return JSON.parse(json);
}

const genTwinsText = readFileSync(
  resolve(root, 'src/game/flagTwins.generated.ts'),
  'utf8',
);
const manualTwinsText = readFileSync(
  resolve(root, 'src/game/flagTwins.ts'),
  'utf8',
);

const ARTWORK_TWIN_GROUPS = extractArrayLiteral(genTwinsText, 'ARTWORK_TWIN_GROUPS');
const NON_GUESSABLE_TWIN_IDS = extractArrayLiteral(genTwinsText, 'NON_GUESSABLE_TWIN_IDS');
const MANUAL_TWIN_GROUPS = extractArrayLiteral(manualTwinsText, 'MANUAL_TWIN_GROUPS');

const TWIN_GROUPS = [...ARTWORK_TWIN_GROUPS, ...MANUAL_TWIN_GROUPS];

// --- map ids ---------------------------------------------------------------
// Mirror src/map/world.ts: a country is a valid quiz target when it appears on
// the map, has metadata, and isn't a non-guessable flag twin. normId drops
// leading zeros to match the numeric ISO ids used everywhere else.
const normId = (id) =>
  id == null || id === 'undefined' ? '' : String(Number(id));

const metaById = new Map(countries.map((c) => [c.id, c]));
const nonGuessable = new Set(NON_GUESSABLE_TWIN_IDS);

const mapIds = new Set();
for (const geom of topo.objects.countries.geometries) {
  const id = normId(geom.id);
  if (!id || !metaById.has(id) || nonGuessable.has(id)) continue;
  mapIds.add(id);
}

// --- emit Rust -------------------------------------------------------------
const rsStr = (s) => `"${String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;

const sorted = [...countries].sort((a, b) => Number(a.id) - Number(b.id));

const countryRows = sorted
  .map(
    (c) =>
      `    Country { id: ${rsStr(c.id)}, alpha2: ${rsStr(c.alpha2)}, ` +
      `continent: ${rsStr(c.continent)}, area: ${Number(c.area).toFixed(1)}, ` +
      `un_member: ${c.unMember ? 'true' : 'false'} },`,
  )
  .join('\n');

const mapIdRows = [...mapIds]
  .sort((a, b) => Number(a) - Number(b))
  .map((id) => rsStr(id))
  .join(', ');

const countriesRs = `// GENERATED by scripts/gen-server-data.mjs — do not edit by hand.
// Mirrors src/data/countries.json + src/map/world.ts so the server picks flags
// from exactly the same pool as the client.

#[derive(Debug, Clone, Copy)]
pub struct Country {
    /// Numeric ISO 3166-1 id (no leading zeros), e.g. "840".
    pub id: &'static str,
    /// ISO 3166-1 alpha-2, e.g. "US" (also the flag/avatar code).
    pub alpha2: &'static str,
    /// Continent / region, e.g. "Europe".
    pub continent: &'static str,
    /// Surface area in km².
    pub area: f64,
    /// True for UN member states (drives the "un" scope filter).
    pub un_member: bool,
}

pub static COUNTRIES: &[Country] = &[
${countryRows}
];

/// Numeric ISO ids that are present and guessable on the world map.
pub static MAP_IDS: &[&str] = &[${mapIdRows}];
`;

const twinRows = TWIN_GROUPS.map(
  (g) => `    &[${g.map(rsStr).join(', ')}],`,
).join('\n');

const twinsRs = `// GENERATED by scripts/gen-server-data.mjs — do not edit by hand.
// Mirrors src/game/flagTwins.ts: groups of numeric ISO ids whose flags are
// indistinguishable. Guessing any member of a group counts as correct.

pub static TWIN_GROUPS: &[&[&str]] = &[
${twinRows}
];
`;

const outDir = resolve(root, 'server/src/game');
mkdirSync(outDir, { recursive: true });
writeFileSync(resolve(outDir, 'countries.rs'), countriesRs);
writeFileSync(resolve(outDir, 'flag_twins.rs'), twinsRs);

console.log(
  `wrote server/src/game/countries.rs (${sorted.length} countries, ${mapIds.size} map ids)`,
);
console.log(`wrote server/src/game/flag_twins.rs (${TWIN_GROUPS.length} twin groups)`);
