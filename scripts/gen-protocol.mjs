// Regenerates the client's protocol types from the Rust source of truth
// (server/src/ws/protocol.rs). The `#[derive(TS)]` types export one .ts file per
// type when the server tests run; server/.cargo/config.toml points
// TS_RS_EXPORT_DIR at src/online, so ts-rs writes straight into
// src/online/bindings/. We then (re)write the barrel src/online/protocol.ts.
// Run: npm run gen-protocol
import { execFileSync } from 'node:child_process';
import { readdirSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const serverDir = resolve(root, 'server');
const onlineDir = resolve(root, 'src/online');
const dstBindings = resolve(onlineDir, 'bindings');

console.log('exporting protocol types (cargo test export_bindings_)…');
execFileSync('cargo', ['test', 'export_bindings_', '--quiet'], {
  cwd: serverDir,
  stdio: 'inherit',
});

if (!existsSync(dstBindings)) {
  throw new Error(`expected generated bindings at ${dstBindings}`);
}

const names = readdirSync(dstBindings)
  .filter((f) => f.endsWith('.ts'))
  .map((f) => f.replace(/\.ts$/, ''))
  .sort();

const barrel =
  '// GENERATED by scripts/gen-protocol.mjs — do not edit by hand.\n' +
  '// Re-exports the ts-rs bindings generated from server/src/ws/protocol.rs.\n' +
  names.map((n) => `export type { ${n} } from './bindings/${n}';`).join('\n') +
  '\n';
writeFileSync(resolve(onlineDir, 'protocol.ts'), barrel);

console.log(`synced ${names.length} protocol types -> src/online/bindings/`);
