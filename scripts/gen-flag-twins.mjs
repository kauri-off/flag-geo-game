// Build-time generation of the "indistinguishable flag" groups.
//
// Many countries render with pixel-identical flags because dependent territories
// fly their parent nation's flag (the French overseas departments, Heard Island,
// St Helena, ...). In the flag-icons set each ISO code gets its own SVG, but for
// these the artwork is byte-identical apart from the `id="flag-icons-xx"`
// attribute — so a flag-only quiz can't fairly tell them apart.
//
// Rather than hand-maintaining that list, we derive it from the artwork itself:
//   1. normalise every flag SVG (strip the id attribute + whitespace) and group
//      codes whose artwork is identical;
//   2. map alpha-2 codes to the numeric ISO ids the game uses;
//   3. keep only ids that are actually on the map AND have metadata (guessable),
//      and only groups that still have >= 2 such members — i.e. pairs that can
//      really collide in play.
// The result is written to src/game/flagTwins.generated.ts.
//
// Usage:
//   node scripts/gen-flag-twins.mjs          regenerate the module
//   node scripts/gen-flag-twins.mjs --check  fail (exit 1) if it's stale
//
// Perceptual near-twins that flag-icons renders DIFFERENTLY (Romania/Chad,
// Indonesia/Monaco) are not caught here by design — they live as a small manual
// supplement in flagTwins.ts.
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const norm = (id) => {
  if (id == null || id === 'undefined') return '';
  const n = Number(id);
  return Number.isFinite(n) ? String(n) : '';
};

// --- on-map, guessable ids -------------------------------------------------
// Mirror src/map/world.ts + src/game/pool.ts: an id is guessable when it is
// present in the world TopoJSON AND has country metadata.
const countries = require('../src/data/countries.json');
const idByAlpha2 = new Map(countries.map((c) => [c.alpha2.toLowerCase(), c.id]));
const metaIds = new Set(countries.map((c) => c.id));
const metaById = new Map(countries.map((c) => [c.id, c]));

const topo = require('../src/assets/countries-110m.json');
const topoObj = topo.objects[Object.keys(topo.objects)[0]];
const mapIds = new Set(topoObj.geometries.map((g) => norm(g.id)).filter(Boolean));

const guessable = (id) => metaIds.has(id) && mapIds.has(id);

// --- group flags by identical artwork --------------------------------------
const flagsDir = resolve(root, 'node_modules/flag-icons/flags/4x3');
const byArtwork = new Map();
for (const file of readdirSync(flagsDir)) {
  if (!file.endsWith('.svg')) continue;
  const alpha2 = file.slice(0, -4).toLowerCase();
  const id = idByAlpha2.get(alpha2);
  if (!id || !guessable(id)) continue; // skip non-countries / off-map codes
  const art = readFileSync(resolve(flagsDir, file), 'utf8')
    .replace(/\s+id="[^"]*"/g, '')
    .replace(/\s+/g, ' ')
    .trim();
  if (!byArtwork.has(art)) byArtwork.set(art, []);
  byArtwork.get(art).push(id);
}

const groups = [...byArtwork.values()]
  .filter((g) => g.length > 1)
  .map((g) => g.sort((a, b) => Number(a) - Number(b)))
  .sort((a, b) => Number(a[0]) - Number(b[0]));

// Within each identical-flag group, ONE representative stays guessable and the
// rest become non-guessable, so the quiz never shows a flag the player can't map
// to a unique answer. The representative is the group's UN member (every group
// here has exactly one); if that's ever ambiguous, fall back to the largest by
// area for a stable, sensible pick (keep the sovereign, drop the territories).
const canonical = (group) => {
  const members = group.map((id) => metaById.get(id));
  const unMembers = members.filter((m) => m.unMember);
  const pick = unMembers.length === 1 ? unMembers : members;
  return pick.slice().sort((a, b) => b.area - a.area)[0].id;
};
const nonGuessable = groups
  .flatMap((g) => {
    const keep = canonical(g);
    return g.filter((id) => id !== keep);
  })
  .sort((a, b) => Number(a) - Number(b));

// --- emit -------------------------------------------------------------------
const groupsBody = groups
  .map((g) => `  [${g.map((id) => `'${id}'`).join(', ')}],`)
  .join('\n');
const nonGuessableBody = nonGuessable.map((id) => `'${id}'`).join(', ');
const content = `// GENERATED by scripts/gen-flag-twins.mjs — do not edit by hand.
// Run \`npm run gen-flag-twins\` to refresh, or \`npm run gen-flag-twins -- --check\`
// to verify it is up to date. See the script for how these are derived.
//
// Groups of numeric ISO ids whose flag-icons artwork is pixel-identical and that
// are both on the map (so they can actually collide in a flag-only quiz).
export const ARTWORK_TWIN_GROUPS: string[][] = [
${groupsBody}
];

// One representative of each group above stays guessable (the sovereign / UN
// member); these are the remaining members, which fly the same flag and so are
// marked non-guessable in src/map/world.ts — never a target, never selectable.
export const NON_GUESSABLE_TWIN_IDS: string[] = [${nonGuessableBody}];
`;

const outRel = 'src/game/flagTwins.generated.ts';
const out = resolve(root, outRel);
const check = process.argv.includes('--check');

if (check) {
  let existing = '';
  try {
    existing = readFileSync(out, 'utf8');
  } catch {
    /* missing -> treated as stale below */
  }
  if (existing !== content) {
    console.error(
      `${outRel} is stale. Run \`npm run gen-flag-twins\` and commit the result.`,
    );
    process.exit(1);
  }
  console.log(`${outRel} is up to date (${groups.length} groups).`);
} else {
  writeFileSync(out, content);
  console.log(`wrote ${outRel} (${groups.length} groups)`);
}
